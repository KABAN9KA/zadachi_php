<!-- задача 4 про выделение красным -->
<?php
 $text = 'Дан текст и "слово", выделить красным цветом слова в тексте которые содержат в себе "слово". 
 Например: Дано слово "мастер" и текст "Форум веб-мастеров"';
 
 $word = 'слово';
 
 $pattern = preg_quote($word, '~');
 $replace = '<span style="color: red">$0</span>';
 
 echo preg_replace("~$pattern~", $replace, $text);
?>